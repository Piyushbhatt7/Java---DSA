public class Recursion_problem2 {
    
   static void patter1(int n)
   {
      for(int i=0; i<n; i++)
      {
         for(int j=0; i<i; j++)
         {
            System.out.println("*");
         }
         System.out.println();
      }
   }

    public static void main(String[] args) {
        
    }
}
