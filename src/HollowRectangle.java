
import java.util.Scanner;

public class HollowRectangle {
    public static void main(String[] args) {
         
        int i,j, n, m;
        System.out.println("Enter row number: ");
        Scanner sc = new Scanner(System.in);
        n = sc.nextInt();
        System.out.println("Enter column number: ");
        m = sc.nextInt(); 

        // outer loop

        for(i =1 ; i<=n ; i ++){

            // inner loop

            for(j = 1 ; j<=m ; j++){
                
                // cell -> i,j
                System.out.println();
            }
        }
        
    }
}
